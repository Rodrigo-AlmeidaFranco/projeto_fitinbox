package br.fitinbox.demo.model;

public class TipoProduto {
	private Integer id_tipoProd;
	private String tipoProd;
	
	
	
	public TipoProduto() {
		super();
	}
	public Integer getId_tipoProd() {
		return id_tipoProd;
	}
	public void setId_tipoProd(Integer id_tipoProd) {
		this.id_tipoProd = id_tipoProd;
	}
	public String getTipoProd() {
		return tipoProd;
	}
	public void setTipoProd(String tipoProd) {
		this.tipoProd = tipoProd;
	}
	
	
	
}
